<h3><b>Ingredients</b></h3></br>
<pre>
1 tbsp Honey
2 1/2 cup cantaloupe Balls
1 tsp Lemon Juice
4 cup Spinach
1 tblsp Water
5 cup Lettuce
1 1/2 cup sliced Strawberry
1 tblsp Balsamic Vinegar
</pre>
<h3><b>How to make</b></h3></br>
<pre>
Mix chopped spinach, chopped lettuce, cantaloupe, and strawberries in a large dish.
    Mix together honey, vinegar, water and lemon juice and pour over the salad.
    Toss well to coat all ingredients and serve at once
</pre>
